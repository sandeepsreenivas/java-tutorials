package variables;

public class TypeCasting {

    public static void main(String[] args) {

        int a = (byte) 10;
        int num1 = 10, num2 = 20;
        float div = (float) num1/num2;
        float num3 = 10f, num4 = 1.6f;
        float div2 = num3/num4;
        System.out.println(div);
        System.out.println(div2);

    }




}
